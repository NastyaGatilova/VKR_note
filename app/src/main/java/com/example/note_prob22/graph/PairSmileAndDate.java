package com.example.note_prob22.graph;

public class PairSmileAndDate {
    private String smile;
    private String dates;

    public PairSmileAndDate(String smile, String dates) {
        this.smile = smile;
        this.dates = dates;
    }

    public String getSmile() {
        return smile;
    }

    public String getDates() {
        return dates;
    }
}
